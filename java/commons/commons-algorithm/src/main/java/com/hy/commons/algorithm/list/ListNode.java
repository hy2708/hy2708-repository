package com.hy.commons.algorithm.list;

public class ListNode {
	private Object data=null;
	public ListNode next=null;

	
	
	public ListNode(Object data) {
		super();
		this.data = data;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}
	

}
